#!/usr/bin/env perl
use warnings;
use strict;
#Shujun Ou (oushujun@iastate.edu) 
#v0.1 08/10/2019
#v0.2 03/05/2026 Add maxdiv and mincov

my $usage = "\nCount all-versus-all misclassifications using the cleanup_nested.pl .stat file
	perl count_nested.pl -in sequence.fa.stat -cat [redun|nested|all] [options] > sequence.fa.stat.sum
		-maxdiv	[int]	Maximum divergence (100-identity) to include. Default: 100 (no filter)
		-mincov	[float]	Minimum reciprocal coverage for redun category. Default: 0.95
\n";

my $input = ''; #input file .stat generated by cleanup_nested.pl
my $cat = "all"; #all: all input entries; redun: only redundant entries (Discarded types); nested: only nested entries (Cleaned types)
my $maxdiv = 100; #max divergence (100 - identity); 100 = no filter
my $mincov = 0.95; #min reciprocal coverage for redun; 0 = no filter

my $k=0;
foreach (@ARGV){
	$input = $ARGV[$k+1] if /^-in$/i and $ARGV[$k+1] !~ /^-/;
	$cat = lc $ARGV[$k+1] if /^-cat$/i and $ARGV[$k+1] !~ /^-/;
	$maxdiv = $ARGV[$k+1] if /^-maxdiv$/i and $ARGV[$k+1] !~ /^-/;
	$mincov = $ARGV[$k+1] if /^-mincov$/i and $ARGV[$k+1] !~ /^-/;
	$k++;
	}

die $usage unless $cat =~ /all|nested|redun/;
open IN, "<$input" or die $usage;
print "Confusion matrix of $input for the $cat category (maxdiv: $maxdiv)\n";

my %stat;
my %types;
while (<IN>){
	s/\.$//;
	# Skip corrupted rows from multithreading (valid rows have at least 10 fields)
	my @fields = split;
	next if scalar(@fields) < 10;
	next if (/Discarded/ and $cat eq "nested");
	next if (/Cleaned/ and $cat eq "redun");

	# Divergence filter
	my $identity = 0;
	$identity = $1 if /identity:\s+([\d.]+)/;
	next if $identity !~ /^\d+\.?\d*$/; # skip corrupted identity values
	next if $identity > 0 and (100 - $identity) > $maxdiv;

	# Reciprocal coverage filter (redun only)
	if ($cat eq "redun" and $mincov > 0) {
		my ($qcov, $scov) = (0, 0);
		$qcov = $1 if /qcov:\s+([\d.]+)/;
		$scov = $1 if /scov:\s+([\d.]+)/;
		next if $qcov !~ /^\d+\.?\d*$/ or $scov !~ /^\d+\.?\d*$/; # skip corrupted values
		# Only apply filter if both fields are present (backward compat with old .stat files)
		next if ($qcov > 0 and $scov > 0) and ($qcov < $mincov or $scov < $mincov);
	}

	s/MITE/DNA/gi;
	s/TIR/DNA/gi;
	my ($type1, $type2) = ($1, $2) if /[\||#]([0-9a-z\/_]+)\s+.*[\||#]([0-9a-z\/_]+)(;|\s+)/i;
	next unless defined $type1 and defined $type2;
	# Skip truncated type names from corrupted lines (valid types have >=2 chars after /)
	next if ($type1 =~ /\// and $type1 !~ /\/.{2,}/) or ($type2 =~ /\// and $type2 !~ /\/.{2,}/);
	$types{$type1}++;
	$types{$type2}++;
	$stat{"$type1-$type2"}++;
	}
close IN;

# remove single-event categories
while (my ($key, $value) = each (%types)) {
	delete $types{$key} if $value <= 1;
	}

my @types = sort {$a cmp $b} keys %types;

# Compute column widths for aligned output
my $col0_w = 0;
for my $t (@types) {
	$col0_w = length($t) if length($t) > $col0_w;
}

my @col_w;
for my $i (0..$#types) {
	$col_w[$i] = length($types[$i]);
	for my $type1 (@types) {
		$stat{"$type1-$types[$i]"} = 0 unless exists $stat{"$type1-$types[$i]"};
		my $vw = length($stat{"$type1-$types[$i]"});
		$col_w[$i] = $vw if $vw > $col_w[$i];
	}
}

my $rate_label = "Inconsistent_rate";
my $rate_w = length($rate_label);

# Print header
printf "%-*s", $col0_w, "";
for my $i (0..$#types) {
	printf "  %*s", $col_w[$i], $types[$i];
}
printf "  %*s\n", $rate_w, $rate_label;

# Print rows
foreach my $type1 (@types) {
	printf "%-*s", $col0_w, $type1;
	$stat{"$type1-$type1"} = 0 unless exists $stat{"$type1-$type1"};
	my $true = $stat{"$type1-$type1"};
	my $all = 0;
	for my $i (0..$#types) {
		$stat{"$type1-$types[$i]"} = 0 unless exists $stat{"$type1-$types[$i]"};
		$all += $stat{"$type1-$types[$i]"};
		printf "  %*d", $col_w[$i], $stat{"$type1-$types[$i]"};
	}
	my $Misclas_rate = 0;
	$Misclas_rate = sprintf("%.4f", ($all-$true)/$all) if $all > 0;
	printf "  %*s\n", $rate_w, $Misclas_rate;
}

